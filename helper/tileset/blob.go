// BlobIndex256 calculates the index of any possible situation (0.255)
// for wang blob tileset based on its neighbors within a 3x3 radius.
func BlobIndex256(n *Neighbors) int {
	index := 0

	if n.Top {
		index += 1
	}
	if n.TopRight {
		index += 2
	}
	if n.Right {
		index += 4
	}
	if n.BottomRight {
		index += 8
	}
	if n.Bottom {
		index += 16
	}
	if n.BottomLeft {
		index += 32
	}
	if n.Left {
		index += 64
	}
	if n.TopLeft {
		index += 128
	}

	return index % 256
}
